public class Strings {
    public static void main (String[] args) {
       String s = "some text";
       String s2 = "some more text";
       System.out.println(s + s2);
       System.out.println(s.split("m",1));
       System.out.println(s.length());
       System.out.println(s.concat(s2));
    }
}
